class Solution(object):
    def search(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: int
        """
        n=len(nums)
        start=0
        end=n-1
        while(start<=end):
            mid=start+(end-start)/2
            if nums[mid]==target:
                return mid
            elif nums[mid]<target:
                start=mid+1
            elif nums[mid]>target:
                end=mid-1
            
        return -1